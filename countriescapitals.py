number_of_questions = 10
current_answer = False
total_correct = 0
total_asked = 0

import random

def set_current_question():
    pair = random.choice(countriescapitals())
    country_or_capital = random.choice([1,2])
    global current_answer
    if country_or_capital == 1:
        current_answer = pair[1]
        return "".join(["What is the capital of ",pair[0],"?"])
    else:
        current_answer = pair[0]
        return "".join(["What country is ",pair[1]," in?"])

def answer_question(answer):
    global total_correct
    global total_asked
    if current_answer == answer:
        total_correct = total_correct + 1
    total_asked = total_asked + 1
    if number_of_questions > total_asked:
        return " ".join([str(number_of_questions - total_asked),
                         "questions left\n",set_current_question()])
    else:
        return "".join(["Your final score is ",str(total_correct),
                       " out of ",str(number_of_questions)])

def set_number_of_questions(x):
    global number_of_questions
    number_of_questions = x

def start_game():
    global number_of_questions
    global total_correct
    global total_asked
    number_of_questions = 5
    total_correct = 0
    total_asked = 0
    return set_current_question()
